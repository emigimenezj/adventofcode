const ROCK = 'rock';
const PAPER = 'paper';
const SCISSORS = 'scissors';
const LOST = 'lost';
const DRAW = 'draw';
const WON = 'won';
const pointsForShape = {
	[ROCK] : 1,
	[PAPER] : 2,
	[SCISSORS] : 3
}
const pointsForMatch = {
	[LOST]: 0,
	[DRAW]: 3,
	[WON]: 6
}
const matchesEntries = data.split('\n').map(match => match.split(' '));



// PART 1
const resultsPerMatch = {
	[ROCK]: {
		[PAPER]: LOST,
		[ROCK]: DRAW,
		[SCISSORS]: WON
	},
	[PAPER]: {
		[SCISSORS]: LOST,
		[PAPER]: DRAW,
		[ROCK]: WON
	},
	[SCISSORS]: {
		[ROCK]: LOST,
		[SCISSORS]: DRAW,
		[PAPER]: WON
	}
}
const shapeParse = {
	A: ROCK,
	B: PAPER,
	C: SCISSORS,
	X: ROCK,
	Y: PAPER,
	Z: SCISSORS
}

function part1() {
	const totalScore = matchesEntries.reduce((rec, [l1, l2]) => {
		const shape1 = shapeParse[l1];
		const shape2 = shapeParse[l2];

		const result = resultsPerMatch[shape2][shape1];

		return rec + pointsForMatch[result] + pointsForShape[shape2];
	}, 0);
	console.log(totalScore);
}



// PART 2
const selectShape = {
	[LOST]: {
		[ROCK]: SCISSORS,
		[PAPER]: ROCK,
		[SCISSORS]: PAPER
	},
	[DRAW]: {
		[ROCK]: ROCK,
		[PAPER]: PAPER,
		[SCISSORS]: SCISSORS
	},
	[WON]: {
		[ROCK]: PAPER,
		[PAPER]: SCISSORS,
		[SCISSORS]: ROCK
	}
}
const letterParse = {
	A: ROCK,
	B: PAPER,
	C: SCISSORS,
	X: LOST,
	Y: DRAW,
	Z: WON
}

function part2() {
	const totalScore = matchesEntries.reduce((rec, [l1, l2]) => {
		const shape = letterParse[l1];
		const result = letterParse[l2];

		const selectedShape = selectShape[result][shape];

		return rec + pointsForMatch[result] + pointsForShape[selectedShape];
	}, 0);
	console.log(totalScore);
}



